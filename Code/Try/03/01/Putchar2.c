#include <stdio.h>
int main()
{
    putchar('F');           //输出字符常量F
    putchar('i');           //输出字符常量i
    putchar('n');           //输出字符常量n
    putchar('d');           //输出字符常量d
    putchar('\0');          //字符结束标志
    putchar('D');           //输出字符常量D
    putchar('a');           //输出字符常量a
    putchar('y');           //输出字符常量y
    putchar('!');           //输出字符常量!
    putchar('\n');          //进行换行
    return 0;
}